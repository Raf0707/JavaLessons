public class IfElse {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        if (a < b) {
            System.out.println("а меньше b");
        } else if (a > b) {
            System.out.println("а больше b");
        } else {
            System.out.println("a = b");
        }

        // ДЗ - такая же контструкция, только с >=, <=
    }
}
